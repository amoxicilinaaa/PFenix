Table = {}

function Table.val_to_str(v)
  if "string" == type(v) then
    v = string.gsub( v, "\n", "\\n" )
    if string.match( string.gsub(v,"[^'\"]",""), '^"+$' ) then
      return "'" .. v .. "'"
    end
    return '"' .. string.gsub(v,'"', '\\"' ) .. '"'
  else
    return "table" == type( v ) and Table.tostring( v ) or
      tostring( v )
  end
end

function Table.key_to_str(k)
  if "string" == type(k) and string.match(k, "^[_%a][_%a%d]*$") then
    return k
  else
    return "[" .. Table.val_to_str( k ) .. "]"
  end
end

function Table.tostring(tbl)
  if not tbl then
    return false
  end

  if type(tbl) ~= "table" then
    return false
  end

  local result, done = {}, {}
  for k, v in ipairs( tbl ) do
    table.insert( result, Table.val_to_str( v ) )
    done[ k ] = true
  end
  for k, v in pairs( tbl ) do
    if not done[ k ] then
      table.insert( result,
        Table.key_to_str( k ) .. "=" .. Table.val_to_str( v ) )
    end
  end
  return "{" .. table.concat( result, "," ) .. "}"
end

table.tostring = Table.tostring

function setAsCurrentAddon(itemUid, addon, outfit)
	local addons = getPokeAddons(itemUid)
	
	local tab = {"head", "body", "legs", "feet"}
	
	for index, inf in pairs(tab) do
		if outfit[inf] then
			doItemSetAttribute(itemUid, "current_addon_"..inf, outfit[inf])
		end
	end
	
	if addon == "normal" then
		doItemSetAttribute(itemUid, "current_addon", "normal")
		return true
	end
	
	for index, info in pairs(addons) do
		if addon == info then
			doItemSetAttribute(itemUid, "current_addon", addon)
			return true
		end
	end
	
	return false
end

function getCurrentAddon(itemUid)
	local current_addon = getItemAttribute(itemUid, "current_addon")

	if type(current_addon) == "number" then
		doItemSetAttribute(itemUid, "current_addon", "normal")
		current_addon = "normal"
	end

	if not current_addon then
		current_addon = "normal"
	end
	
	return current_addon
end

function addAddon(itemUid, addon)
	local addons = getPokeAddons(itemUid)
	
	for index, info in pairs(addons) do
		if addon == info then
			return false
		end
	end

	table.insert(addons, addon)
	
	doItemSetAttribute(itemUid, "addons", table.tostring(addons))

	local str = "Contains a "..getItemAttribute(itemUid, "poke").."."
	if getItemAttribute(itemUid, "nick") then
		str = str.."\nIt's nickname is: "..getItemAttribute(itemUid, "nick").."."
	end

	if #addons > 0 then
			str = str.."\nThis Pok?n has "..#addons.." addons: {"
			local size = 0
			for index, info in pairs(addons) do
				str = str..info
				if size < #addons-1 then
						str = str..", "
				end
				size = size + 1
			end
			str = str.."}."
	end

	doItemSetAttribute(itemUid, "description", str.."\n")
	
	return true
end

function getPokeAddons(itemUid)

	if type(itemUid) == "string" then
		local t = {}
		for index, info in pairs(poke_outfits[itemUid]) do
			if index ~= "normal" then
				table.insert(t, index)
			end
		end
		return t
	end

	local addons_info = getItemAttribute(itemUid, "addons")

	if type(addons_info) == "number" then
		addons_info = "{}"
	end

	if not addons_info then
		addons_info = "{}"
	end

	local t = loadstring("return "..addons_info)()
	
	if not t then
		t = {}
	end
	
	return t
	
end

function getPokeOutfitColors(itemUid, pokemon, addonType)

	local ret = {lookHead = getItemAttribute(itemUid, "current_addon_head"), lookBody = getItemAttribute(itemUid, "current_addon_body"), lookLegs = getItemAttribute(itemUid, "current_addon_legs"), lookFeet = getItemAttribute(itemUid, "current_addon_feet")}

	if pokemon then
		local addon = getCurrentAddon(itemUid)

		if not addonType then
			addonType = "normal"
		end
		
		local lookType
		if not poke_outfits[pokemon] or not poke_outfits[pokemon][addon] or not poke_outfits[pokemon][addon][addonType] then
			lookType = 229 -- Debug for moltres value
			print(string.format(">> Unable to retrieve addon %s at addons_config from %s, fix this at addons_config.lua", addon, pokemon))
		else
			lookType = poke_outfits[pokemon][addon][addonType]
		end
		
		ret.lookType = lookType
	end

	return ret
end

